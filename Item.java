/**
 * Class Item - a item in an game.
 *
 * This class is part of the "Fly-Away" application. 
 * "Fly-Away" is a very simple, text based adventure game.  
 *
 * A "Item" represents one object in a room or backpack. 
 * 
 * Every item has a name, weight, whether item can be picked and type and cost.
 * 
 * @author  733474
 * @version 18.03.15
 */
public class Item
{
     private String name;
     private boolean pickable;
     private int weight;
     private Type type;
     private double cost;
     
     /**
      * Create a item's "name", "pickable", "weight", "type" and "cost".
      * @param name the items name
      * @param pickable the items pick value(if its pickable)
      * @param weight the items weight
      * @param type the items type
      * @param cost the items cost
      */
     public Item(String name, boolean pickable, int weight, Type type, double cost)
     {
          this.name = name;
          this.pickable = pickable;
          this.weight = weight;
          this.type = type;
          this.cost = cost;
     }
     
     /**
      * @return The items name.
      */
     public String getName()
     {
          return name;
     }
     
     /**
      * @return The items pickable value.
      */
     public boolean getPickable()
     {
          return pickable;
     }
     
     /**
      * @return The items weight.
      */
     public int getWeight()
     {
          return weight;
     }
     
     /**
      * @return The items type.
      */
     public Type getType()
     {
          return type;
     }
     
     /**
      * @return The items cost.
      */
     public double getCost()
     {
          return cost;
     }
}